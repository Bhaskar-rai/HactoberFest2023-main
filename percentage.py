# Get three numbers from the user
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))
num3 = float(input("Enter the third number: "))

# Perform addition, multiplication, and subtraction
addition_result = num1 + num2 + num3
multiplication_result = num1 * num2 * num3
subtraction_result = num1 - num2 - num3

# Display the results
print(f"Addition Result: {addition_result}")
print(f"Multiplication Result: {multiplication_result}")
print(f"Subtraction Result: {subtraction_result}")
